# Tuples in python
# tuples are un changable
Tuples1 = (12, 6, -8, 'Jenny', True) # creating tuples
print(Tuples1) # printing tuples
print(Tuples1[-2]) # index printing
print(Tuples1[1]) # negative index printing
print(type(Tuples1))
Tuples2= (45) # this is not tuples ',' is must
print(type(Tuples2))
Tuples3= (45,) # tuples
print(type(Tuples3))
Tuples4 = (15, 17, 18, 23, 29, 35, 54)
print(Tuples4[3:6])